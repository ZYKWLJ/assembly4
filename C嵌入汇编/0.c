#include <stdio.h>

int main()
{
    int x = 3;
    int y;

    // 内嵌汇编代码，实现 y = x * 5 的功能
    __asm__ __volatile__(
        "leal (%1, %1, 4), %0"
        : "=r"(y) // 输出操作数，将计算结果存入 y，使用任意通用寄存器（r 约束）
        : "0"(x)  // 输入操作数，x 使用和输出操作数 y 相同的寄存器（0 约束表示复用 %0 对应的寄存器）
    );

    // 输出结果，验证是否正确计算了 x * 5
    printf("x = %d, y = x * 5 = %d\n", x, y);

    return 0;
}